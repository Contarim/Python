n1=float(input('Digite sua nota de Matemática do primeiro Bimestre: '))
n2=float(input('Digite sua nota de Matemática do segundo Bimestre: '))
m= (n1 + n2) / 2

#Fazendo uma média escolar a partir de 2 notas
print('Você tirou {} no primeiro bimestre e {} no segundo bimestre, a sua média é {:.1f}'.format(n1,n2,m))