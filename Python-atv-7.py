n=input('Digite algo: ')

#Teste dos tipos de variáveis e tipos de formatação

print('O tipo da variável é:',type(n));
print('Está tudo em maiúsculo? ',n.isupper());
print('Está tudo em minúsculo? ',n.islower());
print('Contém apenas letras? ',n.isalpha());
print('Contém apenas números? ',n.isnumeric());
print('Contém letras e números? ',n.isalnum());
print('Estpa capitalizada? ',n.istitle());
